
public class Charactor {
    String name;
    String weapon;
    int energyValue;
    
    void setName(String k) { name = k;}
    void setWeapon(String k) { weapon = k;}
    void setEnergyValue(int j) { energyValue = j; }
    
    String getName() { return name; }
    String getWeapon() { return weapon; }
    int getEnergyValue() { return energyValue; }
}
